-- ============================================================
-- profiles にログイン用パスワード（ハッシュ）列を追加
--   - 認証なし運用のため Supabase Auth は使わない。
--   - 入口(/login)で利用者を選んだ後、このハッシュと照合してログインする。
--   - 平文は保存せず SHA-256 ハッシュ(hex)のみ保存する（クライアントでハッシュ化）。
--     ⚠️ anon キーで読める前提のため強固な認証ではない。社内/限定配布向けの簡易ロック。
--   - 既存利用者は password_hash = null。null の場合はパスワードなしでログインできる
--     （初回利用時に本人がマイページで設定、または管理者が設定する想定）。
-- SQL Editor に貼って Run。
-- ============================================================

alter table public.profiles
  add column if not exists password_hash text;

-- PostgREST のスキーマキャッシュ再読込
notify pgrst, 'reload schema';
